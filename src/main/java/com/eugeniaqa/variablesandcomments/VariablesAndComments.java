package com.eugeniaqa.variablesandcomments;

public class VariablesAndComments {
    public static void main(String[] args) {
        // Declare section

        boolean exampleOfBooleanVariable = true;
        char exampleOfCharVariable;
        int exampleOfIntVariable;
        byte exampleOfByteVariable;
        short exampleOfShortVariable;
        long exampleOfLongVariable;
        float exampleOfFloatVariable;
        double exampleOfDoubleVariable;

        // Intialization section

        exampleOfCharVariable = 'C';
        exampleOfIntVariable = 5646456;
        exampleOfByteVariable = 12;
        exampleOfShortVariable = 32000;
        exampleOfLongVariable = 564564562;
        exampleOfFloatVariable = 64.5f;
        exampleOfDoubleVariable = 545.4564855445;

        System.out.println(exampleOfShortVariable);

    }
}
